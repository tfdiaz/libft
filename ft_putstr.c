#include "libft.h"

void	ft_putstr(char const *s)
{
	while (*s)
		write(1, s++, 1);
}

void	prtputstr(char const *s)
{
	while (*s)
	{
		if (*s == 7)
			write(1, "\0", 1);
		else
			write(1, s, 1);
		s++;
	}
}
